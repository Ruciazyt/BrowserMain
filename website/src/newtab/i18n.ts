export type MessageKey = keyof typeof messages.en;

export function resolveLocale(preference?: AppLocale, systemLocale?: string): string {
  if (preference === 'zh-CN') return 'zh-CN';
  if (preference === 'en') return 'en-US';
  return systemLocale || navigator.language || 'en-US';
}

function interpolate(template: string, vars?: Record<string, string | number>): string {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? `{${key}}`));
}

export function useI18n() {
  const { settings } = useSettings();
  const [systemLocale, setSystemLocale] = useState(() => navigator.language || 'en-US');

  useEffect(() => {
    const handler = () => setSystemLocale(navigator.language || 'en-US');
    window.addEventListener('languagechange', handler);
    return () => window.removeEventListener('languagechange', handler);
  }, []);

  const locale = useMemo(() => resolveLocale(settings.locale, systemLocale), [settings.locale, systemLocale]);
  const language: Language = locale.startsWith('zh') ? 'zh' : 'en';

  const t = useMemo(() => {
    return (key: MessageKey, vars?: Record<string, string | number>) => interpolate((messages[language] as Record<string, string>)[key], vars);
  }, [language]);

  return {
    locale,
    language,
    isZh: language === 'zh',
    t,
  };
}